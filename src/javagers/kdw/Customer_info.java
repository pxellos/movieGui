package javagers.kdw;

public class Customer_info {
	private String customer_name;
	private String customer_id;
	private String customer_pwd;
	private String customer_birth;
	private String customer_addr;
	private String customer_gen;
	private String customer_num;
	private int customer_pnt;
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_pwd() {
		return customer_pwd;
	}
	public void setCustomer_pwd(String customer_pwd) {
		this.customer_pwd = customer_pwd;
	}
	public String getCustomer_birth() {
		return customer_birth;
	}
	public void setCustomer_birth(String customer_birth) {
		this.customer_birth = customer_birth;
	}
	public String getCustomer_addr() {
		return customer_addr;
	}
	public void setCustomer_addr(String customer_address) {
		this.customer_addr = customer_address;
	}
	public String getCustomer_gen() {
		return customer_gen;
	}
	public void setCustomer_gen(String customer_gen) {
		this.customer_gen = customer_gen;
	}
	public String getCustomer_num() {
		return customer_num;
	}
	public void setCustomer_num(String customer_num) {
		this.customer_num = customer_num;
	}
	public int getCustomer_pnt() {
		return customer_pnt;
	}
	public void setCustomer_pnt(int customer_pnt) {
		this.customer_pnt = customer_pnt;
	}
	
}
