public class Screen_Info {
	
	private int screen_screen;
	private int screen_round;
	private String screen_begin;
	private String screen_end;
	private String screen_mname;
	public int getScreen_round() {
		return screen_round;
	}
	public void setScreen_round(int screen_round) {
		this.screen_round = screen_round;
	}
	public int getScreen_screen() {
		return screen_screen;
	}
	public void setScreen_screen(int screen_screen) {
		this.screen_screen = screen_screen;
	}

	public String getScreen_begin() {
		return screen_begin;
	}
	public void setScreen_begin(String screen_begin) {
		this.screen_begin = screen_begin;
	}
	public String getScreen_end() {
		return screen_end;
	}
	public void setScreen_end(String screen_end) {
		this.screen_end = screen_end;
	}
	public String getScreen_mname() {
		return screen_mname;
	}
	public void setScreen_mname(String screen_mname) {
		this.screen_mname = screen_mname;
	}

	
}
